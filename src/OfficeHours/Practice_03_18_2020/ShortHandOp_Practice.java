package OfficeHours.Practice_03_18_2020;

public class ShortHandOp_Practice {
    public static void main(String[] args) {
        int a = 200;
        a += 100; // addition// a= 200+100
        System.out.println(a);

        a -= 60*3; // Subtraction // a= 300 -(60*3)=120
        System.out.println(a);

        a *=3 + 5; // a= 120 * (3+5)=960
        System.out.println(a);

        a /= 3; //320
        System.out.println(a);

        a %= 3;
        System.out.println(a); // 2 oldu



    }
}
